def detect_type(keyword):
    mapping = {
        "환급": "refund", "연금": "pension", "보험": "insurance",
        "청약": "housing", "대출": "loan",
        "세금": "tax", "세액": "tax", "공제": "tax",
    }
    for key, typ in mapping.items():
        if key in keyword:
            return typ
    return "general"


def make_writing_guide(keyword):
    """기존 app.py와의 호환을 위해 소제목 리스트만 반환"""
    typ = detect_type(keyword)
    content = TYPE_CONTENT.get(typ, TYPE_CONTENT["general"])
    return [title for title, _ in content["sections"]]


def generate_outline(keyword):
    """제목/체크리스트 UI용 요약 개요"""
    typ = detect_type(keyword)
    content = TYPE_CONTENT.get(typ, TYPE_CONTENT["general"])
    return {
        "intro": content["intro"],
        "sections": [f"{i+1}. {title}" for i, (title, _) in enumerate(content["sections"])],
        "faq": [q for q, a in content["faq"]],
        "image_suggestions": [
            f"{keyword} 관련 공식 안내/캡처 이미지",
            "체크리스트 또는 표 형태 인포그래픽",
            "본문 중간 핵심 요약 카드형 이미지",
        ],
        "tag_suggestions": [keyword.replace(" ", ""), "정리", "총정리", "확인방법"],
    }


def generate_full_draft(keyword, difficulty="보통"):
    """
    네이버 블로그/티스토리에 바로 복사해서 쓸 수 있는 수준의 본문 초안을 생성한다.
    API 키 없이도 이 함수만으로 실제 문장이 채워진 글이 나온다.
    """
    typ = detect_type(keyword)
    content = TYPE_CONTENT.get(typ, TYPE_CONTENT["general"])

    lines = []
    lines.append(f"# {keyword} 총정리\n")
    lines.append(content["intro"] + "\n")

    for title, body in content["sections"]:
        lines.append(f"## {title}")
        lines.append(body.replace("{kw}", keyword) + "\n")

    lines.append("## FAQ")
    for q, a in content["faq"]:
        lines.append(f"**Q. {q}**")
        lines.append(f"A. {a}\n")

    lines.append("## 마무리")
    lines.append(content["closing"] + "\n")

    if difficulty == "어려움":
        lines.append(
            "※ 본 글은 정보 제공을 목적으로 작성되었으며, 세부 조건은 관련 기관 공고나 "
            "전문가 상담을 통해 다시 확인하시길 권장합니다.\n"
        )

    return "\n".join(lines)
